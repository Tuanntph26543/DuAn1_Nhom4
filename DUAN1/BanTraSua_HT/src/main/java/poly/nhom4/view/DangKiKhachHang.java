    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        String input1 = txtTim.getText();
        String input2 = txtTim2.getText();
        int count = 0;
        if (input1 != null || input2 != null) {
            for (int i = 0; i < reponses.size(); i++) {
                if (input1.equalsIgnoreCase(reponses.get(i).getTenKH().toString())) {
                    tbKH.setRowSelectionInterval(i, i);
                    count++;
                }
                if (input2.equalsIgnoreCase(reponses.get(i).getSdt().toString())) {
                    tbKH.setRowSelectionInterval(i, i);
                    count++;
                }

            }
        }
        if (count == 0) {
            JOptionPane.showMessageDialog(rootPane, "Không tìm thấy thông tin khách hàng");
        }
    }//GEN-LAST:event_btnTimActionPerformed

