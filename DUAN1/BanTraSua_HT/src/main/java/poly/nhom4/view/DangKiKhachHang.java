    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        row = tbKH.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn bản ghi");
        } else {
            int choice = JOptionPane.showConfirmDialog(this, "Chắc chắn cập nhật?", "UPDATE", 0);
            if (choice == JOptionPane.YES_OPTION) {
                KhachHangReponse kh = getKH();
                kh.setMaKH(ser.getAll().get(row).getMaKH());
                JOptionPane.showMessageDialog(this, ser.update(kh));
                addrow(new KhachHangServiceIplm().getAll());
                btnNewActionPerformed(evt);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        row = tbKH.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn bản ghi");
        } else {
            int choice = JOptionPane.showConfirmDialog(this, "Xóa không?", "DELETE", 0);
            if (choice == JOptionPane.YES_OPTION) {
                KhachHangReponse kh = getKH();
                kh.setMaKH(Integer.parseInt(txtMa.getText()));
                JOptionPane.showMessageDialog(this, ser.delete(kh));
                addrow(new KhachHangServiceIplm().getAll());
                btnNewActionPerformed(evt);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

