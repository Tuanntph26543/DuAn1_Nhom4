    public void addrow(List<KhachHangReponse> list) {
        defaultTableModel = (DefaultTableModel) tbKH.getModel();
        defaultTableModel.setRowCount(0);
        for (KhachHangReponse x : list) {
            defaultTableModel.addRow(new Object[]{
                x.getMaKH(), x.getTenKH(), x.getSdt(), x.getDiaChi()
            });
        }

    }

    private KhachHangReponse getKH() {
        KhachHangReponse kh = new KhachHangReponse();
        kh.setTenKH(txtTen.getText());
        kh.setSdt(txtSdt.getText());
        kh.setDiaChi(txtDc.getText());
        return kh;
    }

    private void fillForm() {
        KhachHangReponse kh = new KhachHangServiceIplm().getAll().get(row);
        txtMa.setText(kh.getMaKH().toString());
        txtTen.setText(kh.getTenKH().toString());
        txtSdt.setText(kh.getSdt().toString());
        txtDc.setText(kh.getDiaChi().toString());
    }

    public Boolean check() {
        if (txtTen.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống Họ tên", "ERROR", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        String phone = "(((\\+|)84)|0)(3|5|7|8|9)+([0-9]{8})\\b";
        if (txtSdt.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Nhập Số điện thoại", "ERROR", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if ((!txtSdt.getText().trim().matches(phone))) {
            JOptionPane.showMessageDialog(this, "SĐT đúng định dạng của Việt Nam (+84)", "ERROR", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (txtDc.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống Địa chỉ", "ERROR", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

