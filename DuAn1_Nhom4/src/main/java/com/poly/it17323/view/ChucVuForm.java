    private void btnFindDspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindDspActionPerformed
        // TODO add your handling code here:
        String input = txtTim.getText();
        int count = 0;
        if (input != null) {
            for (int i = 0; i < reponses.size(); i++) {
                if (input.equalsIgnoreCase(reponses.get(i).getMaCV().toString())) {
                    tbCV.setRowSelectionInterval(i, i);
                    count++;
                }
            }
        }
        if (count == 0) {
            JOptionPane.showMessageDialog(rootPane, "Ko tìm thấy chức vụ");
        }
    }//GEN-LAST:event_btnFindDspActionPerformed

