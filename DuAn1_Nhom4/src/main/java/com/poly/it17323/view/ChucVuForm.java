    private void showData(List<ChucVuReponse> list) {
        model = (DefaultTableModel) tbCV.getModel();
        model.setRowCount(0);
        list.forEach(s -> model.addRow(s.toDataRow()));
    }

    private ChucVu getCV() {
        ChucVu cv = new ChucVu();
        cv.setTenCV(txt_tencv.getText());
        return cv;
    }

    private ChucVu getData() {
        int ma = Integer.parseInt(txt_ma.getText());
        String tenCV = txt_tencv.getText();
        return new ChucVu(ma, tenCV);
    }

    private void clear() {
        txt_ma.setText("");
        txt_tencv.setText("");
    }

    private void fillForm() {
        ChucVuReponse cv = reponses.get(row);
        txt_ma.setText(cv.getMaCV().toString());
        txt_tencv.setText(cv.getTenCV());
    }

