    private void btnXoaDspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaDspActionPerformed
        // TODO add your handling code here:
        row = tbCV.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn bản ghi");
        } else {
            int choice = JOptionPane.showConfirmDialog(this, "Xóa không?", "Delete", 0);
            if (choice == JOptionPane.YES_OPTION) {
                ChucVu cv = getData();
                JOptionPane.showMessageDialog(this, ser.delete(cv));
                showData(new ChucVuServiceImpl().getAll());
                clear();
            }
        }
    }//GEN-LAST:event_btnXoaDspActionPerformed

